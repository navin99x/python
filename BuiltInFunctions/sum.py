#  calculates the total of all elements in an iterable

numbers = [1, 2, 3, 4, 5]
total = sum(numbers)
print(f"Sum of numbers: {total}")
# Output: Sum of numbers: 15

# Using sum() with an optional start value
numbers = [10, 20, 30]
print(f"Sum with start value 50: {sum(numbers, 50)}")
# Output: Sum with start value 50: 110
